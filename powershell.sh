# How to run Zig code in Windows Powershell

```sh
PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Zig\hello-world> zig run ./src/main.zig
PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Rust> cargo run --bin hello_world

PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Zig\error_handling> zig run ./src/main.zig
PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Rust> cargo run --bin error_handling

PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Zig\memory_management> zig run ./src/main.zig
PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Rust> cargo run --bin memory_management

PS D:\ZigProjects\Zig_Rust_Comparison\Hello_World\Zig\concurrency> zig run ./src/main.zig
